package org.github.armanddu.boardgame.board;

public class BoardDimension {

  private int width;
  private int height;

  public BoardDimension(int width, int height) {
    this.width = width;
    this.height = height;
  }

  public int getWidth() {
    return this.width;
  }

  public int getHeight() {
    return this.height;
  }

}
